DROP TABLE CLUB
CASCADE CONSTRAINTS;

DROP SEQUENCE CLUB_SEQ;
--CLUBMENT

CREATE TABLE CLUB(
		CLUBSEQ NUMBER CONSTRAINT CLUB_PRI PRIMARY KEY, 
		CLUBTITLE VARCHAR2(200),
		CLUBTEXT VARCHAR2(4000),
		CLUBDATE DATE,
		CLUBIMAGE VARCHAR2(100),
		CLUBDEL NUMBER,
		ID VARCHAR2(20),
		HEADERNUM NUMBER,
  		CONSTRAINT FK_CLUB FOREIGN KEY (ID) 
        REFERENCES MEMBER(ID)
)

ALTER SEQUENCE CLUB_SEQ
