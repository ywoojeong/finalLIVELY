DROP TABLE APPLYCOMMENT
CASCADE CONSTRAINTS;

DROP SEQUENCE ACOM_SEQ;

--APPLYCOMMENT

CREATE TABLE APPLYCOMMENT(
		APPCOMSEQ NUMBER,
		APPCOMCONTENT VARCHAR2(2000),
		APPCOMDATE DATE,
		APPCOMDEL NUMBER,
		APPLYBBSSEQ NUMBER,
		LIKESEQ NUMBER,
		EMAIL VARCHAR2(50),			
  		CONSTRAINT FK_APPCOM FOREIGN KEY (LIKESEQ) 
        REFERENCES MYLIKE(LIKESEQ),
        CONSTRAINT FK_APPCOM2 FOREIGN KEY (EMAIL) 
        REFERENCES MEMBER(EMAIL)
)


CREATE SEQUENCE ACOM_SEQ
START WITH 10000
INCREMENT BY 1

